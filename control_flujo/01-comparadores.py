"""
    En este archivo se van a ver los comparadores logicos
    estos comparadores devuelven True o False si es que
    se cumple la condicion.
    """

print(2 > 2)  # Devuelve False
print(2 < 3)  # Devuelve True
print(2 <= 2)  # Devuelve True
print(2 >= 5)  # Devuelve False
print(2 == 2)  # Devuelve True
print(2 != 1)  # Devuelve True
