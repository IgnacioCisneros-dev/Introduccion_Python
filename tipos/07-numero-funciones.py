import math


"""
    Aqui se van a presentar metodos o funciones que se pueden 
    usar con numeros, igual se puede ver algunas funciones del
    modulo math
    """

print(round(1.3))  # Nos regresar el numero entero al cual este mas cercano
print(round(1.8))  # En este caso no regresa el numero entero 2

print(abs(-77))  # Nos regresa el numero entero, es decir, sin el signo de negativo

"""
    Uso del modulo Math 
    """
print(math.ceil(1.1))  # Redondea un decimal hacia arriba
print(math.floor(1.99999))  # Redondea un decimal hacia abajo
print(math.isnan(2))  # Retorna
