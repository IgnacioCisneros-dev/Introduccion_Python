
# Ejemplo de como crear listas de diferentes tipo

numeros = [1, 2, 3]
letras = ["a", "b", "c"]
palabras = ["chancito", "feliz"]
booleans = [True, False, False]
matriz = [[1, 2], [3, 4], [5, 6]]
ceros = [0] * 10
rangos = list(range(1, 11))
chars = ["Ignacio Cisneros"]
union_de_listas = palabras + booleans + chars

print(f"Numeros {numeros}")
print(f"Letras {letras}")
print(f"Palabras {palabras}")
print(f"Booleans {booleans}")
print(f"Matriz {matriz}")
print(f"Ceros {ceros}")
print(f"Rangos {rangos}")
print(f"Chars {chars}")
print(f"Union de listas {union_de_listas}")
